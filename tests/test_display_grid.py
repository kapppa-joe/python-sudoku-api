from sudoku_api.core.display import display_grid


def test_display_grid():
    puzzle1 = "1234341223414123"
    expected_output = """+-----+-----+
| 1 2 | 3 4 |
| 3 4 | 1 2 |
+-----+-----+
| 2 3 | 4 1 |
| 4 1 | 2 3 |
+-----+-----+"""

    assert display_grid(puzzle1, 2, 2) == expected_output

    puzzle2 = "000000270008270045040000008000567010005009007000040000200000401900010000650304792"
    expected_output2 = """+-------+-------+-------+
| . . . | . . . | 2 7 . |
| . . 8 | 2 7 . | . 4 5 |
| . 4 . | . . . | . . 8 |
+-------+-------+-------+
| . . . | 5 6 7 | . 1 . |
| . . 5 | . . 9 | . . 7 |
| . . . | . 4 . | . . . |
+-------+-------+-------+
| 2 . . | . . . | 4 . 1 |
| 9 . . | . 1 . | . . . |
| 6 5 . | 3 . 4 | 7 9 2 |
+-------+-------+-------+"""

    assert display_grid(puzzle2, 3, 3) == expected_output2
